let currentLang = localStorage.getItem('shahajjo_lang') || 'en';
const listeners = new Set();

export const i18n = {
  get lang() {
    return currentLang;
  },
  set lang(val) {
    if (val === 'en' || val === 'bn') {
      currentLang = val;
      localStorage.setItem('shahajjo_lang', val);
      listeners.forEach(cb => cb(val));
    }
  },
  t(key) {
    return translations[currentLang][key] || translations['en'][key] || key;
  },
  onChange(cb) {
    listeners.add(cb);
    return () => listeners.delete(cb);
  }
};
